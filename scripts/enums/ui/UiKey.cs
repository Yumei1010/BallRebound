namespace BallRebound.scripts.enums.ui;

/// <summary>
///     定义 UI 页面键值枚举，用于标识不同的 UI 面板
/// </summary>
public enum UiKey
{
    /// <summary>
    ///     模板主页面
    /// </summary>
    TemplatePage,

    /// <summary>
    ///     主菜单页面
    /// </summary>
    MainMenu,

    /// <summary>
    ///     游戏内 HUD 页面（能量条、连击、分数、计时器）
    /// </summary>
    GameHud,

    /// <summary>
    ///     暂停菜单页面
    /// </summary>
    PauseMenu,

    /// <summary>
    ///     设置菜单页面
    /// </summary>
    Settings,

    /// <summary>
    ///     统计数据页面
    /// </summary>
    Stats,

    /// <summary>
    ///     游戏结束页面
    /// </summary>
    GameOver,
}
